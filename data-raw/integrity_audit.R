  list(fig = "fig7_detection.png",
       claim = "COG-UK",
       check = "Uses surv_detection_probability on real seq volumes")
)

for (fc in fig_claims) {
  exists <- file.exists(file.path("man/figures", fc$fig))
  # Check README mentions the right data source
  fig_lines <- grep(fc$fig, readme, ignore.case = TRUE)
  if (length(fig_lines) > 0) {
    # Look at surrounding text for data source claim
    context_start <- max(1, fig_lines[1] - 5)
    context_end <- min(length(readme), fig_lines[1] + 10)
    context <- paste(readme[context_start:context_end], collapse = " ")
    
    if (fc$claim == "Simulated") {
      honest <- grepl("[Ss]imulat", context)
    } else {
      honest <- grepl("COG-UK|real", context, ignore.case = TRUE)
    }
  } else {
    honest <- FALSE
  }
  
  status <- if (exists && honest) "OK" else "CHECK"
  if (!honest) issues <- issues + 1L
  cat(sprintf("  %s %s: file=%s, claim=%s, honest=%s\n",
              status, fc$fig, exists, fc$claim, honest))
}

