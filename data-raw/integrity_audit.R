cat("\n[3.3] '0.4 pp mean difference'\n")
weighted <- surv_lineage_prevalence(design, "XBB.1.5", method = "hajek")
naive <- surv_naive_prevalence(design, "XBB.1.5")
actual_diff <- mean(abs(weighted$estimates$prevalence -
                        naive$estimates$prevalence), na.rm = TRUE)
cat("  Actual mean |diff|:", round(actual_diff * 100, 1), "pp\n")
